function add(arg1, arg2) {
    return arg1 + arg2;
}

function sub(arg1, arg2) {
    return arg1 - arg2;
}

function mul(arg1, arg2) {
    return arg1 * arg2;
}

function div(arg1, arg2) {
    return arg1 / arg2;
}

console.log(add(1, 2));
console.log(sub(1, 2));
console.log(mul(1, 2));
console.log(div(1, 2));